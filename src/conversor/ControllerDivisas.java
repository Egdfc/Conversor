package conversor;

public class ControllerDivisas {
	
	private static String divisaDadaM;
	private static String divisaSolicitadaM;
	private static double valordado;
	private static double valorSale;
	
	
	public String getDivisaDadaM() {
		return divisaDadaM;
	}
	public void setDivisaDadaM(String divisaDadaM) {
		this.divisaDadaM = divisaDadaM;
	}
	public String getDivisaSolicitadaM() {
		return divisaSolicitadaM;
	}
	public void setDivisaSolicitadaM(String divisaSolicitadaM) {
		this.divisaSolicitadaM = divisaSolicitadaM;
	}
	public double getValordado() {
		return valordado;
	}
	public void setValordado(double valordado) {
		this.valordado = valordado;
	}
	public double getValorSale() {
		return valorSale;
	}
	public void setValorSale(double valorSale) {
		this.valorSale = valorSale;
	}
	
	

}
